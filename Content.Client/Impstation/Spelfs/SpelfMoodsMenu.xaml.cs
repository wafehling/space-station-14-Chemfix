using Content.Client.UserInterface.Controls;
using Content.Shared.Impstation.Spelfs.Components;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.XAML;

namespace Content.Client.Impstation.Spelfs;

[GenerateTypedNameReferences]
public sealed partial class SpelfMoodsMenu : FancyWindow
{
    public SpelfMoodsMenu()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);
    }

    public void Update(EntityUid uid, SpelfMoodsBuiState state)
    {
        MoodDisplayContainer.Children.Clear();

        foreach (var mood in state.SharedMoods)
            MoodDisplayContainer.AddChild(new MoodDisplay(mood, true));

        foreach (var mood in state.Moods)
            MoodDisplayContainer.AddChild(new MoodDisplay(mood, false));
    }
}
